package forExample;

public class App {

    public static void main(String[] args) {

        for (int index = 10; index >= 0; index--) {
            System.out.println("First for loop: " + index);
        }

        for (int i = 0; i <= 10; i++) {
            System.out.println("Second for loop: " + i);

        }
    }
}
