package basicSyntax;

public class App {

    /**
     * Case sensitivity: Java is case sensitive
     * 'cat' and 'Cat' are different
     * <p>
     * public static void main(String args[]) this is the method
     * where every program starts in Java
     * ~ so it is mandatory
     * <p>
     * class names: the first letter should be in upper case
     * Or if we have several words: each word's first letter is upper case
     * <p>
     * For example: Cat, Dog, StringBuilder, SortingAlgorithm
     * <p>
     * method names: all method names should start with lower case
     * <p>
     * For example: public void printNames()
     */

    public static void main(String[] args) {
        System.out.println("Hello world from Java");
    }
}
